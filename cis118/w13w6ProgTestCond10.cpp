#include <iostream>
using namespace std;
int main() {
    // Sample program to print the value of i at each loop..

    int i = 0;  // 1st element of loop declare an initialize value

    while ((i + 3) < 10) {  // 2nd element of loop u2013 condition/criteria
        cout << "i = " << i << endl;
        i++;  // 3rd element of loop update value of condition variable
    }

    return 0;
}
